#include "libft.h"

int		ft_isascii(int c)
{
	unsigned char g;

	if (c >= 0 && c <= 255)
	{
		g = (unsigned char)c;
		if (g <= 127 && g >= 0)
			return (1);
	}
	return (0);
}
