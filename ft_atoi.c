#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int a;
	int j;

	a = 0;
	i = 0;
	j = 0;
	while (str[i] == '\n' || str[i] == '\t' || str[i] == ' ' ||
			str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-')
		j = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		a = a * 10 + str[i] - '0';
		i++;
	}
	if (j == 1)
		return (-a);
	else
		return (a);
}
