int		ft_isalnum(int c)
{
	unsigned char g;

	if (c >= '0' && c <= 255)
	{
		g = (unsigned char)c;
		if ((g >= '0' && g <= '9') || (g >= 'A' && g <= 'Z') ||
				(g >= 'a' && g <= 'z'))
			return (1);
	}
	return (0);
}
