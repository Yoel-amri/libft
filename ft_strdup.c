#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*p;
	int		len;

	i = 0;
	len = 0;
	while (s1[len] != '\0')
		len++;
	if ((p = (char*)malloc(sizeof(*s1) * (len + 1))) == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		p[i] = s1[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
